// ===== THEME — sadece başlangıç, toggle components.js'de =====
(function () {
  const html = document.documentElement;
  if (!html.getAttribute('data-theme')) {
    const prefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
    html.setAttribute('data-theme', prefersDark ? 'dark' : 'light');
  }
})();

// ===== i18n =====
function applyLang(lang) {
  currentLang = lang;
  const t = i18n[lang];
  if (!t) return;
document.querySelectorAll('[data-i18n]').forEach(el => {
  const key = el.getAttribute('data-i18n');
  if (t[key] !== undefined) {
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t[key];
    } else {
      el.innerHTML = t[key]; // ← textContent yerine innerHTML
    }
  }
});
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.getAttribute('data-lang') === lang);
  });
  document.documentElement.lang = lang === 'TR' ? 'tr' : lang === 'FR' ? 'fr' : 'en';
}

function initLangSwitcher() {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.getAttribute('data-lang')));
  });
  applyLang(currentLang);
}

// ===== ACTIVE NAV LINK =====
function setActiveNav() {
  const path = window.location.pathname;
  document.querySelectorAll('.navbar-links a, .mobile-nav a').forEach(a => {
    const href = a.getAttribute('href') || '';
    const isActive = (path.endsWith('index.html') || path === '/' || path === '')
      ? href.includes('index.html') || href === './' || href === '/'
      : path.includes(href.replace('./', '').replace('.html', '')) && href.length > 2;
    a.classList.toggle('active', isActive);
  });
}

// ===== SCROLL REVEAL =====
function initScrollReveal() {
  if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) return;
  const obs = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        e.target.classList.add('visible');
        obs.unobserve(e.target);
      }
    });
  }, { threshold: 0.12 });
  document.querySelectorAll('.reveal').forEach(el => obs.observe(el));
}

// ===== INIT =====
document.addEventListener('DOMContentLoaded', () => {
  initLangSwitcher();
  setActiveNav();
  initScrollReveal();
});